//Operadores de comparação
//Verdadeiro(true) ou falso(false)


//igualdade
let a = 78
let b = "78"

console.log( a == b); //true porque sao iguais
console.log(a === b); //false porque nao sao identicos pois tipos de variaveis diferentes

let numero = 5
let numero2 = "5"
console.log(numero == numero2);
console.log(numero != numero2);
console.log(numero === numero2)

a = "a" //ja foram declaradas acima
b = 5 //ja foram declarada acima
let c = "5"
let d = "fernando"
console.log(a == b);
console.log(a === c);
console.log(c == d);
console.log(b === d);
